namespace HyperfTest;

use Elasticsearch\Client;
use Hyperf\Testing;
use PHPUnit\Framework\TestCase;

/**
 * Class HttpTestCase.
 * @method get($uri, $data = [], $headers = [])
 * @method post($uri, $data = [], $headers = [])
 * @method json($uri, $data = [], $headers = [])
 * @method file($uri, $data = [], $headers = [])
 */
abstract class HttpTestCase extends TestCase
{
    /**
     * @var Testing\Client
     */
    protected $client;

    /**
     * @var string
     */
    protected $version;

    public function __construct($name = null, array $data = [], $dataName = '')
    {
        parent::__construct($name, $data, $dataName);
        $this->client = make(Testing\Client::class);
        // $this->client = make(Testing\HttpClient::class, ['baseUri' => 'http://127.0.0.1:9501']);
    }

    public function __call($name, $arguments)
    {
        return $this->client->{$name}(...$arguments);
    }

    protected function format(Client $client, array $params)
    {
        if (empty($this->version)) {
            $this->version = $client->info()['version']['number'];
        }

        if (version_compare($this->version, '7.0.0', '>=')) {
            unset($params['type']);
        }

        return $params;
    }
}
